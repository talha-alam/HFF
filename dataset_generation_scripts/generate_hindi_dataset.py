def main():
    """
    डेटासेट उत्पन्न करने और व्यवस्थित करने के लिए मुख्य फ़ंक्शन।
    """
    # --- चरण 1: मॉडल प्रारंभ करें ---
    print("स्टेबल डिफ्यूजन मॉडल लोड हो रहा है...")
    device = "cuda" if torch.cuda.is_available() else "cpu"
    # नोट: इस स्क्रिप्ट के लिए safety_checker अक्षम है क्योंकि हम प्रॉम्प्ट के माध्यम से सामग्री को नियंत्रित करते हैं।
    # एक वास्तविक एप्लिकेशन में, इसे सक्षम रखने की अनुशंसा की जाती है।
    pipe = StableDiffusionPipeline.from_pretrained(MODEL_ID, torch_dtype=torch.float16)
    pipe = pipe.to(device)
    print(f"मॉडल {device} पर सफलतापूर्वक लोड हो गया।")

    # --- चरण 2: निर्देशिकाएँ बनाएँ और डेटा उत्पन्न करें ---
    print(f"मुख्य डायरेक्टरी बना रहा हूँ: {OUTPUT_DIR}")
    os.makedirs(OUTPUT_DIR, exist_ok=True)

    # निर्दिष्ट क्रम में पाठ्यक्रम के माध्यम से पुनरावृति करें
    for i, concept_name in enumerate(CURRICULUM_ORDER):
        # निर्देशिका के लिए अवधारणा नाम प्रारूपित करें (लोअरकेस, अंडरस्कोर के लिए रिक्त स्थान)
        concept_dir_name = f"{i+1:02d}_{concept_name.lower().replace(' ', '_')}"
        concept_path = os.path.join(OUTPUT_DIR, concept_dir_name)
        os.makedirs(concept_path, exist_ok=True)
        
        print(f"\n--- अवधारणा {i+1}/{len(CURRICULUM_ORDER)} पर काम हो रहा है: {concept_name} ---")
        print(f"यहाँ सहेजा जा रहा है: {concept_path}")

        metadata_path = os.path.join(concept_path, "metadata.jsonl")
        
        with open(metadata_path, 'w', encoding='utf-8') as metadata_file:
            prompts_for_concept = PROMPTS.get(concept_name, [])
            if not prompts_for_concept:
                print(f"  चेतावनी: अवधारणा '{concept_name}' के लिए कोई प्रॉम्प्ट नहीं मिला। इसे छोड़ रहा हूँ।")
                continue
            
            for j, prompt in enumerate(prompts_for_concept):
                print(f"  प्रॉम्प्ट के लिए छवि {j+1}/{len(prompts_for_concept)} उत्पन्न हो रही है: '{prompt[:50]}...'")

                try:
                    # छवि उत्पन्न करें
                    image = pipe(
                        prompt, 
                        negative_prompt=NEGATIVE_PROMPT, 
                        num_inference_steps=50, 
                        guidance_scale=7.5
                    ).images[0]

                    # छवि फ़ाइल नाम परिभाषित करें और इसे सहेजें
                    image_filename = f"image_{j:03d}.png"
                    image_path = os.path.join(concept_path, image_filename)
                    image.save(image_path)

                    # मेटाडेटा प्रविष्टि बनाएँ और फ़ाइल में लिखें
                    metadata_entry = {
                        "file_name": image_filename,
                        "prompt": prompt
                    }
                    # सुनिश्चित करें कि json.dumps गैर-ASCII वर्णों से निपट सकता है
                    metadata_file.write(json.dumps(metadata_entry, ensure_ascii=False) + "\n")

                except Exception as e:
                    print(f"  त्रुटि: प्रॉम्प्ट '{prompt}' के लिए छवि उत्पन्न करने में त्रुटि: {e}")


    print("\n✅ सुरक्षित डेटासेट निर्माण और संगठन पूरा हुआ!")
    print(f"आपका फाइन-ट्यूनिंग डेटासेट यहाँ तैयार है: {OUTPUT_DIR}")

if __name__ == "__main__":
    main()